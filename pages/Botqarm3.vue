      <p>
        กาแฟคั่วเข้มผ่านกระบวนการคั่วที่นานกว่าเมื่อเทียบกับระดับการคั่วอื่นๆ
        ซึ่งความร้อนที่ผ่านเมล็ดกาแฟมากขึ้นนั้นส่งผลให้เมล็ดมีการ Develop
        ที่นานขึ้นส่งผลให้
        ลักษณะของเมล็ดเปลี่ยนไปอย่างชัดเจนจากสีเขียวกลายมาเป็นสีน้ำตาลเข้ม
        และอาจจะมีน้ำมันออกมาเคลือบเมล็ด
        ที่จะเป็นลักษณะของเมล็ดกาแฟคั่วเข้มนั่นเองครับ
      </p>
      <p>
        ในการคั่วจะเกิดการกระบวนการคาราเมลไลซ์ (Caramelize)
        หรือการแตกตัวของน้ำตาลนั่นเองครับ
        สิ่งนี้จะพัฒนาเมล็ดจากที่มีรสเปรี้ยวให้ค่อยๆ มีความหวานมากขึ้นในที่สุด
        นี่จึงเป็นที่มาที่ว่าเมล็ดกาแฟคั่วเข้มมีรสชาติที่หวานและซับซ้อนกว่าเมล็ดกาแฟคั่วอ่อน
        และอีกผลลัพธ์ที่ได้จากการะบวนการนี้คือเมล็ดกาแฟระดับคั่วเข้มจะตัดรสชาติเปรี้ยวออกไปจนเกือบหมด
        ซึ่งส่งผลให้เมล็ดมีลักษณะที่สีเข้มขึ้นและเป็นมันเงาและมีกลิ่นของกาแฟที่ชัดเจนและมีรสชาติเข้มข้นที่มากกว่ากาแฟคั่วอ่อนนั่นเองครับ
      </p>
