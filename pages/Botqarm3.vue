      <p>
        หนึ่งในคุณสมบัติเด่นของกาแฟคั่วเข้มคือรสชาติที่เข้มข้นและกลิ่นควัน
        (smoky) ที่ชัดเจน โดยมักจะมีกลิ่นของช็อกโกแลต คาราเมล
        กระบวนการคั่วเข้มช่วยให้กาแฟมีระดับความเป็นกรดต่ำลง
        ทำให้รสชาติมีความหวานมากขึ้น และความเปรี้ยวลดลงไปครับ
        แต่ถ้าเป็นการคั่วในระดับที่เข้มเกินไปก็จะเพิ่มความขมเข้ามาได้มากขึ้นครับ
        มากไปกว่านั้นในตอนสกัดกาแฟคั่วเข้มเราจะเจอครีม่าที่ชัดเจนมากกว่ากาแฟคั่วอ่อน
        ซึ่งเกิดจากการคั่วด้วยความร้อนเป็นเวลานานทำให้มีน้ำมันกาแฟออกมา
        ซึ่งน้ำมันกาแฟตรงนี้แหละครับ ที่ทำให้เกิดครีม่านั่นเองครับ
        หลายคนที่ไม่ชอบกาแฟรสชาติเปรี้ยว แต่ชอบที่จะเห็นครีม่าสวยๆ บนกาแฟ
        เมล็ดกาแฟคั่วเข้มถือว่าเป็นคำตอบที่ดีเลยครับ
      </p>
